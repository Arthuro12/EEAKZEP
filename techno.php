    <p> Die Digitalisierung macht auch vor dem Energiesektor nicht Halt. 
    Mit der Einführung des Gesetzes zur Digitalisierung der Energiewende im September 2016 hat Deutschland das Startsignal für innovative Technik gesetzt. 
    Ob Blockchain, Smart Grid oder Big Data – die Begriffe sind in aller Munde.
     Neue Chancen bieten sich vor allen Dingen für Erneuerbare Energien. Denn digitale Technologien ermöglichen es, Wetter- und Verbrauchsdaten zu analysieren und so die Versorgungssicherheit durch Sonnen- und Windenergie zu steigern. 
     Ein anderer Schwerpunkt liegt in der dezentralen Energieversorgung- und dem Energiehandel, die die Digitalisierung mit sich bringt. Verbraucher und Erzeuger können mit digitalen Technologien zu jeder Tageszeit und bei jeder Wetterlage unabhängig von einem zentralen System miteinander über den Erneuerbaren Strompreis verhandeln.

    Damit das Zukunftsszenario von vollständig digitaler Energieversorgung Realität wird, müssen Energieversorger zu Datenexperten werden und Bedürfnisse von Verbrauchern sammeln, analysieren und auswerten, um zur passenden Produktinnovation zu gelangen.
     Die Innovationen von Technologien in der Erneuerbaren-Energie-Branche sind und bleiben wichtiger Fokuspunkt in Forschung und Wirtschaft.                  
