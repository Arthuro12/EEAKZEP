    <section><h1>Wissens- und Datenbasis</h1>
    <p>
    Die konkreten Auswirkungen des Ausbaus erneuerbarer Energien auf Arten und Habitate sind aktuell noch nicht vollumfassend bekannt. 
    Es sollte daher ein Monitoring der Energiewende aus Naturschutzsicht etabliert werden, das die Wirkungen der einzelnen erneuerbaren Energieträger dokumentiert. Eine Möglichkeit dafür stellt der Monitoring-Bericht zum EEG dar. 
    Aufbauend auf dem aktuellen Wissensstand ist eine weitere Verbesserung des Wissens zu den Wirkzusammenhängen notwendig. Die intensive Forschungsarbeit der letzten Jahre macht deutlich, dass innovative Lösungsansätze gefunden werden können. Sie ist daher fortzusetzen und sollte insbesondere aktuelle Entwicklungen wie z. B. verbesserte Erfassungssysteme an Windkraftanlagen berücksichtigen und deren Einführung in die Praxis begleiten. Darüber hinaus sind Erhebungen zur Populationsentwicklung und Gefährdungsursachenanalysen betroffener Arten erforderlich. Auch die weitere Erforschung von Zugverhalten bei Fledermäusen und Vögeln ist von großer Bedeutung.
    Die Lösungsansätze aus zahlreichen Forschungsvorhaben zum Thema naturverträgliche Anlagengestaltung sind in die Praxis zu überführen und anzuwenden. Eine weitere Vernetzung von Wissenschaft und Praxis ist daher unerlässlich, um übergreifende Themenfelder, Synergien und Forschungsbedarf zu identifizieren, Forschungsergebnisse aufzubereiten und in die Praxis zu tragen. Sie ist daher fortzusetzen. 
    Zur Bewertung von kumulativen Wirkungen des EE-Ausbaus ist eine Methodik zu entwickeln, die eine vergleichbare und handhabbare Nutzung in der Praxis ermöglicht. Auch eine weitere Verankerung kumulativer Wirkungsbewertungen in Raumplanung und Umweltprüfung ist aufgrund der zunehmenden Dichte von EE-Anlagen erforderlich.
    Die Erprobungs- und Begleitforschung an EE-Anlagen ist zu intensivieren. Durch die wissenschaftliche Begleitung z. B. an Testanlagen oder auch im regulären Anlagenbetrieb kann die Naturverträglichkeit bewertet werden. So ist beispielsweise auch die Wirksamkeit von Vermeidungsmaßnahmen zu evaluieren.
